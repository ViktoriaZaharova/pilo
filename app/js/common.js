$('.btn-size').click(function (e) {
    e.preventDefault();
    $(this).siblings('.box-hidden').fadeToggle();
});

$('.go_to').click(function () {
    var scroll_el = $(this).attr('href');
    if ($(scroll_el).length != 0) {
        $('html, body').animate({
            scrollTop: $(scroll_el).offset().top
        }, 500);
    }
    return false;
});

